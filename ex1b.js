// Con el operador [] podemos acceder a la posición i-ésima de un string

let nombre = "Pedro";

// Apartado 1. Usa el operador [] en cada una de las líneas sobre la variable 'nombre' para que aparezca lo que pide el console.log

console.log("Debería aparecer 'o'", nombre[4]) // Ejemplo: console.log("Debería aparecer 'o'", nombre[5]) o nombre[nombre.length-1]

console.log("Debería aparecer 'e'", nombre[1]) 

console.log("Debería aparecer 'r'", nombre[3])

console.log("Debería aparecer 'P'", nombre[0]) 